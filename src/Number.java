
public class Number
	{
		private int number;
		private int place;
		public Number(int n, int p)
			{
			number = n;
			place = p; 
			}	
		public int getNumber()
			{
			return number;
			}
		public void setNumber(int number)
			{
			this.number = number;
			}
		public int getPlace()
			{
			return place;
			}
	public void setPlace(int place)
			{
			this.place = place;
			}
		
	}
